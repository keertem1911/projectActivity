package com.technologyActivity.entities;

 
public class Vip {
	private long vip_id	;			
	private String vip_type;
	private double vip_price;	
	private String vip_unit;
 
	public long getVip_id() {
		return vip_id;
	}
	public void setVip_id(long vip_id) {
		this.vip_id = vip_id;
	}
	public String getVip_type() {
		return vip_type;
	}
	public void setVip_type(String vip_type) {
		this.vip_type = vip_type;
	}
	public double getVip_price() {
		return vip_price;
	}
	public void setVip_price(double vip_price) {
		this.vip_price = vip_price;
	}
	public String getVip_unit() {
		return vip_unit;
	}
	public void setVip_unit(String vip_unit) {
		this.vip_unit = vip_unit;
	}
 
	
}
